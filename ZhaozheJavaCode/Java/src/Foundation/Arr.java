public class Arr {

    public static void main(String[] args) {

        int[] arr = new int[4];
        arr[0] = 1;
        arr[1] = 2;
        arr[2] = 3;
        System.out.println(arr[3]);

        String[] strings = {"one","two"};
        System.out.println(strings[1]);

        //定义一个位变量
        byte a = 14;

        //定义一个字符标量
        char c = 'A';

        //定义一个短整型变量
        short value = 15;

        //定义一个整型变量
        int number = 5;

        //定义一个长整型变量
        long money = 2000L;

        //定义一个单精度浮点型变量
        float r = 2.5F;

        //定义一个双精度浮点型变量
        double pi = 3.1415926;

        //定义一个字符串变量
        String name = "zhangsan";



    }
}
